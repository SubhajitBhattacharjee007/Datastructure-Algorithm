package Arrays;

public class Demo {


	private String name;
	private String subject;
	
	public Demo() {
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getSubject() {
		return subject;
	}

	public void setSubject(String subject) {
		this.subject = subject;
	}

	public Demo(String name, String subject) {
		this.name = name;
		this.subject = subject;
	}
}
